package ex_24012025;

public class Lab024 {
    public static void main(String[] args) {


//      Relational Operators -> boolean

        // <, >, >=, <=, ==, !=, (, ), !, =

        int age_anand = 30;
        int age_shailendra = 29;
        boolean result1 = age_anand > age_shailendra;
        boolean result2 = age_anand < age_shailendra;
        boolean result3 = age_anand <= age_shailendra;
        boolean result4 = age_anand >= age_shailendra;
        boolean result5 = age_anand != age_shailendra;


        System.out.println(result1);
        System.out.println(result2);
        System.out.println(result3);
        System.out.println(result4);
        System.out.println(result5);
    }
}
