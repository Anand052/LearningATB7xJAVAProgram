package ex_26012025;

public class Lab039 {
    public static void main(String[] args) {

        // Increment (++) / Decrement (--) Operators
        // Pre and Post
        // Ready

        // Pre - increment- '++' operand(a)
        // value is incremented first and then stored int the result
        int a = 10;
        int b = ++a; // a = a + 1;

        System.out.println(b);
        System.out.println(a);
        System.out.println(b);

        // POST
        // Value is stored in result and then incremented later
        int a1 = 13;
        System.out.println(a1++);
        System.out.println(a1);




    }
}
